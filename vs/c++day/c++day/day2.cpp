#include <iostream>

using namespace std;

int main2(){
	int a0 = 1, a1 = 2;
	double b0 = 1.3, b1 = 0.4;

	cout << "整数相除：" << a0 / a1 << endl << "小数相除：" << b0 / b1 << endl;
	cout << endl;

	/*取模运算时，被除数小于除数时，结果为本身   两个数不能为小数*/
	int c0 = 5, c1 = 10;
	double c2 = 2.5, c3 = 0.25;
	cout << "被除数小于除数时：" << c0 % c1 << endl;
	/*cout << "两个数为小数时：" << c2 % c3 << endl;
	cout << "除数为小数时：" << c1 % c3 << endl;
	cout << "被除数为小数时：" << c2 % c0 << endl;  3种错误*/
	cout << endl;

	int d0 = 10;
	int d3 = 10;
	int d1 = ++d0 * 10;  /*前置++ 先让变量加1，在运算表达式*/
	int d2 = d3++ * 10;  /*后置++ 先运算表达式，在加1*/
	cout << "d0 = " << d0 <<endl;
	cout << "d1 = " << d1 <<endl;
	cout << "d3 = " << d3 <<endl;
	cout << "d2 = " << d2 <<endl;
	cout << endl;

	int score; //分数
	cout << "请输入分数" << endl;
	cin >> score;
	/*判断分数是否超过750分
		超过750分 提醒用户重新输入合理分数
		未超过750分 打印分数*/
	if(score > 750){
		cout << "重新输入合理分数!" << endl;
		cout << "请重新输入分数" << endl;
		cin >> score;
	}else{
		cout << "您的分数为：" << score << "分" << endl;
	}

	//if后面不加分号
	//大于等于600 考上一本大学 输出祝贺语句 
	//在一本分数中，大于700 考入北大 大于650 考入清华 大于等于600 考入人大
	//小于600 大于450 考上二本大学 输出祝福语句  
	//小于450 鼓励用户
	if(score >=  600){ 
		cout << "恭喜您考上一本大学！" << endl;
		if(score > 700){
			cout << "恭喜你考上了北京大学！" << endl;
		}else if(score >650){
			cout << "恭喜你考上了清华大学！" << endl;
		}else{
			cout << "恭喜你考上了中国人民大学！" << endl;
		}
	}else if(score < 600 && score >= 450){
		cout << "恭喜您考上二本大学！" << endl;
	}else{
		cout << "请不要气馁！" << endl;
	}

	system("pause");

	return 0;
}